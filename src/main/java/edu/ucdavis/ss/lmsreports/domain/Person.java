package edu.ucdavis.ss.lmsreports.domain;

import java.util.List;



/**
 * Person represents every user in the system. <br>
 * 
 * In addition to the regular users <b>one</b> user exists that represents the
 * system itself.


 */
public class Person  {

	private int id;
	private String lastName;
	private String firstName;
	//private String location;
	private String ucnetId;
	private int roleId;
	private String RoleName;
	private String email;
	private Location location;
	private String fullName;
	private int location_id;
	private String shibUserId;
	private List<Activity> activityList;
	
	

	
	public List<Activity> getActivityList() {
		return activityList;
	}
	public void setActivityList(List<Activity> activityList) {
		this.activityList = activityList;
	}
	public String getShibUserId() {
		return shibUserId;
	}
	public void setShibUserId(String shibUserId) {
		this.shibUserId = shibUserId;
	}
	public int getLocation_id() {
		return location_id;
	}
	public void setLocation_id(int location_id) {
		this.location_id = location_id;
	}
	public String getFullName() {
		return firstName + " " + lastName;
	}
	public void setFullName(String firstName, String lastName) {
		this.fullName = firstName + " " + lastName;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	
	
	
	public Location getLocation() {
		return location;
	}
	public void setLocation(Location location) {
		this.location = location;
	}
	public String getUcnetId() {
		return ucnetId;
	}
	public void setUcnetId(String ucnetId) {
		this.ucnetId = ucnetId;
	}
	public int getRoleId() {
		return roleId;
	}
	public void setRoleId(int roleId) {
		this.roleId = roleId;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getRoleName() {
		return RoleName;
	}
	public void setRoleName(String roleName) {
		RoleName = roleName;
	}
	
	
	
	
}