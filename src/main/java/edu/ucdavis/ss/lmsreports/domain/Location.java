package edu.ucdavis.ss.lmsreports.domain;


public class Location {
	
	private long locationId;
	private String locationCd;
	private String locationCdHdr;
	private String locationAbbr;
	private String locationName;
	private String locationNameLong;
	private String shibIdProvider;
	private long lmsKey;
	private String revBy;
	private int topLevelInd;
	private int topLevelLMSKey;
	private java.util.Date revDate;


	

	public String getLocationCdHdr() {
		return locationCdHdr;
	}

	public void setLocationCdHdr(String locationCdHdr) {
		this.locationCdHdr = locationCdHdr;
	}

	public String getLocationAbbr() {
		return locationAbbr;
	}

	public void setLocationAbbr(String locationAbbr) {
		this.locationAbbr = locationAbbr;
	}

	public String getLocationNameLong() {
		return locationNameLong;
	}

	public void setLocationNameLong(String locationNameLong) {
		this.locationNameLong = locationNameLong;
	}

	public String getShibIdProvider() {
		return shibIdProvider;
	}

	public void setShibIdProvider(String shibIdProvider) {
		this.shibIdProvider = shibIdProvider;
	}

	public String getLocationCd() {
		return locationCd;
	}

	public void setLocationCd(String locationCd) {
		this.locationCd = locationCd;
	}

	public long getLocationId() {
		return locationId;
	}

	public void setLocationId(long locationId) {
		this.locationId = locationId;
	}

	public String getLocationName() {
		return locationName;
	}

	public void setLocationName(String locationName) {
		this.locationName = locationName;
	}

	


	public long getLmsKey() {
		return lmsKey;
	}

	public void setLmsKey(long lmsKey) {
		this.lmsKey = lmsKey;
	}

	public String getRevBy() {
		return revBy;
	}

	public void setRevBy(String revBy) {
		this.revBy = revBy;
	}


	public java.util.Date getRevDate() {
		return revDate;
	}

	public void setRevDate(java.util.Date revDate) {
		this.revDate = revDate;
	}

	public int getTopLevelInd() {
		return topLevelInd;
	}

	public void setTopLevelInd(int topLevelInd) {
		this.topLevelInd = topLevelInd;
	}

	public int getTopLevelLMSKey() {
		return topLevelLMSKey;
	}

	public void setTopLevelLMSKey(int topLevelLMSKey) {
		this.topLevelLMSKey = topLevelLMSKey;
	}


}
